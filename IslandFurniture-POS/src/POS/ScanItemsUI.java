    public void updateTotal(){
        int rows = jTable.getModel().getRowCount();
        int total = 0;
        int current = 0;
        for (int i=0;i<rows;i++){
            try{
                String val = String.valueOf(jTable.getModel().getValueAt(i, 3));
                current = Integer.parseInt(val);
            }catch(Exception e){
                
            }
            total += current;
            current = 0;
        }
        totalLabel.setText("Grand Total: "+total);
    }

