package IslandFurniture.EJB.Entities;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Entity;
import javax.persistence.OneToMany;
import javax.persistence.PostPersist;

/**
 *
 * @author Chen Tong <chentong@nus.edu.sg>
 */
@Entity
public class CountryOffice extends Plant implements Serializable {

    private static final long serialVersionUID = 1L;
    @OneToMany(mappedBy = "countryOffice")
    private List<Store> stores;

    @OneToMany(mappedBy = "countryOffice")
    private List<ManufacturingFacility> mFacs;

    public List<Store> getStores() {
        return stores;
    }

    public void setStores(List<Store> stores) {
        this.stores = stores;
    }

    public List<ManufacturingFacility> getmFacs() {
        return mFacs;
    }

    public void setmFacs(List<ManufacturingFacility> mFacs) {
        this.mFacs = mFacs;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof CountryOffice)) {
            return false;
        }
        CountryOffice other = (CountryOffice) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "CountryOffice[ id=" + id + " ]";
    }

    // Entity Callbacks
    @PostPersist
    public void postPersist() {
        System.out.println("Successfully persisted " + this);
    }
}
